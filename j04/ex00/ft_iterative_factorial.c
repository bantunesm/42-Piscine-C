int		ft_iterative_factorial(int nb)
{
	int	i;

	i = nb;
	if (nb > 0 && nb < 13)
	{
		while (nb > 1)
		{
			i = i * (nb - 1);
			nb--;
		}
		return (i);
	}
	else
		return (0);
}
